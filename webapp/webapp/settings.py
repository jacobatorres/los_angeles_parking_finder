def get_hostname():
    client = boto3.client('rds')
    response = client.describe_db_instances()
    db_endpoint = ""
    for db_instance in response['DBInstances']:
        db_endpoint = db_instance['Endpoint']['Address']
        if "terraform" in db_endpoint:
            return db_endpoint


